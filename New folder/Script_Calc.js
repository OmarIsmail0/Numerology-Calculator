// Life Path Number calculation function
function calculateLifePathNumber(birthDate) {
  const [year, month, day] = birthDate.split("-").map(Number);

  // Helper function to reduce a number to a single digit
  const reduceToSingleDigit = (num) => {
    let steps = [];

    while (num > 9) {
      steps.push(num);
      num = num
        .toString()
        .split("")
        .reduce((sum, digit) => sum + Number(digit), 0);
    }
    steps.push(num);

    return { final: num, steps: steps };
  };

  // Reducing the day, month, and year to single digits
  const reducedDay = reduceToSingleDigit(day);
  const reducedMonth = reduceToSingleDigit(month);
  const reducedYear = reduceToSingleDigit(year);

  // Calculate the total sum and reduce it to get the Life Path Number
  const totalSum = reducedDay.final + reducedMonth.final + reducedYear.final;
  const lifePathResult = reduceToSingleDigit(totalSum);

  // Calculate the Attitude Number (day + month)
  const attitudeSum = day + month;
  const attitudeResult = reduceToSingleDigit(attitudeSum);

  // Return the results including the Attitude Number
  return {
    lifePathNumber: lifePathResult.final,
    dayReduction: reducedDay,
    monthReduction: reducedMonth,
    yearReduction: reducedYear,
    totalSum: totalSum,
    totalReductionSteps: lifePathResult.steps,
    attitudeNumber: attitudeResult.final, // Added Attitude Number
    attitudeReductionSteps: attitudeResult.steps, // Steps to reach Attitude Number
  };
}

